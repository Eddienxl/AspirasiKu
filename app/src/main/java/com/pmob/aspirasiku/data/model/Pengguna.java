package com.pmob.aspirasiku.data.model;

public class Pengguna {
    private int id;
    private String nim;
    private String nama;
    private String email;
    private String peran;

    public int getId() {
        return id;
    }

    public String getNim() {
        return nim;
    }

    public String getNama() {
        return nama;
    }

    public String getEmail() {
        return email;
    }

    public String getPeran() {
        return peran;
    }
}
